<h3>Access</h3>

<p>The entrance is via the main door of the Kettock Lodge building, we're on the first floor on the right.</p>

<p>If you are not a member of the space but expect that someone is in, then you can ring the buzzer for Suite H and they might let you in.</p>

<p>Members can get the door code from <a href="https://hub.57north.org.uk/hub/door">hub</a>.</p>
    
<h3>Transport Info</h3>

<h4>Busses</h4>
<p>The best bus for getting here is the Firstbus number One, getting off by the science park on Balgownie Road, on the stop just after Balgownie Drive.  You can pick up the number one from RGU and all along Holburn Street, Union Street and King Street.</p>

<h4>Train</h4>
<p>Aberdeen train station is about 3.2 miles away from the hackspace.  If arriving in Aberdeen by train, it is best to get the number one bus from Union Street, a 5 minute walk from the train station.</p>

<h4>Driving / Parking</h4>
<p>Parking at the space is simple and free, there are a few spaces at the front of the building, but many more to the rear.</p>

<h4>Flying</h4>
<p>The hackspace is about 10km from Aberdeen airport.  The simplest option is a taxi, which would probably be between £15 and £20 and would take around 15 minutes.  The other option would be a bus to the city center and then get the number one bus from there.  </p>  
    
